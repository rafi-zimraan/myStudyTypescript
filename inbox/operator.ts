
// Operator aritmatika
// penjumlahan
let bilangan1 = 3;
let bilangan2 = 5;

let penjumlahan = bilangan1 + bilangan2;
console.log( "penjumlahan = ", penjumlahan);

// pengurangan
let pengurangan = bilangan1 - bilangan2;
console.log("pengurangan = ", pengurangan);


// perkalian
let perkalian = bilangan1 * bilangan2;
console.log("perkalian = ", perkalian);


// pembagian
let pembagian = bilangan1 / bilangan2;
console.log("pembagian = " , pembagian);


// pangkat
let pangkat = bilangan1 ** bilangan2;
console.log("pangkat = ", pangkat);     


// sisa bagi
let sisaBagi = bilangan1 % bilangan2;
console.log("sisaBagi = ", sisaBagi);


